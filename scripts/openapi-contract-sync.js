import fs from 'fs-extra';
import path from 'path';

const PRIMITIVE_TYPE_MAP = {
  string: 'string',
  integer: 'number',
  number: 'number',
  boolean: 'boolean',
  array: '[]',
  object: 'Record<string, unknown>'
};

function toPascalCase(str) {
  return str
    .replace(/[-_](.)/g, (_, c) => c.toUpperCase())
    .replace(/^(.)/, (_, c) => c.toUpperCase());
}

function resolveRef(schema, root) {
  if (!schema || !schema.$ref) return schema;
  const refPath = schema.$ref.replace('#/', '').split('/');
  let resolved = root;
  for (const seg of refPath) {
    resolved = resolved?.[seg];
  }
  return resolved || schema;
}

function convertProperty(prop, root, depth = 0) {
  if (depth > 5) return 'unknown';

  prop = resolveRef(prop, root);

  if (prop.allOf) {
    const resolved = prop.allOf.map(p => convertProperty(p, root, depth + 1));
    return resolved.join(' & ');
  }

  if (prop.oneOf) {
    const resolved = prop.oneOf.map(p => convertProperty(p, root, depth + 1));
    return resolved.join(' | ');
  }

  if (prop.enum) {
    return prop.enum.map(v => `'${v}'`).join(' | ');
  }

  if (prop.type === 'array') {
    const itemType = convertProperty(prop.items || { type: 'string' }, root, depth + 1);
    return `${itemType}[]`;
  }

  if (prop.type === 'object' && prop.properties) {
    const entries = Object.entries(prop.properties);
    const fields = entries.map(([key, val]) => {
      const tsType = convertProperty(val, root, depth + 1);
      const optional = prop.required?.includes(key) ? '' : '?';
      return `  ${key}${optional}: ${tsType}`;
    });
    return `{\n${fields.join(';\n')};\n}`;
  }

  return PRIMITIVE_TYPE_MAP[prop.type] || 'unknown';
}

function generateInterface(name, schema, root) {
  const resolved = resolveRef(schema, root);

  if (!resolved.properties) {
    return `export type ${name} = ${convertProperty(resolved, root)};\n`;
  }

  const entries = Object.entries(resolved.properties);
  const fields = entries.map(([key, val]) => {
    const tsType = convertProperty(val, root);
    const optional = resolved.required?.includes(key) ? '' : '?';
    const comment = val.description ? `  /** ${val.description} */\n` : '';
    return `${comment}  ${key}${optional}: ${tsType};`;
  });

  return `export interface ${name} {\n${fields.join('\n')}\n}\n`;
}

function generateApiFunction(method, path, operation, root) {
  const operationId = operation.operationId || `${method}_${path.replace(/[{}\/]/g, '_')}`;
  const funcName = operationId.replace(/[-_](.)/g, (_, c) => c.toUpperCase());

  const params = [];
  const pathParams = (operation.parameters || []).filter(p => p.in === 'path');
  const queryParams = (operation.parameters || []).filter(p => p.in === 'query');

  for (const p of pathParams) {
    params.push(`${p.name}: string | number`);
  }
  if (queryParams.length > 0) {
    params.push(`params?: { ${queryParams.map(q => `${q.name}?: ${PRIMITIVE_TYPE_MAP[q.schema?.type] || 'string'}`).join('; ')} }`);
  }

  const bodySchema = operation.requestBody?.content?.['application/json']?.schema;
  if (bodySchema) {
    const resolved = resolveRef(bodySchema, root);
    const typeName = resolved.title || bodySchema.$ref?.split('/').pop() || 'unknown';
    params.push(`data: ${toPascalCase(typeName)}`);
  }

  const responseSchema = operation.responses?.['200']?.content?.['application/json']?.schema;
  let returnType = 'void';
  if (responseSchema) {
    const resolved = resolveRef(responseSchema, root);
    if (resolved.$ref) {
      returnType = toPascalCase(resolved.$ref.split('/').pop());
    } else if (resolved.type === 'array' && resolved.items?.$ref) {
      returnType = `${toPascalCase(resolved.items.$ref.split('/').pop())}[]`;
    } else {
      returnType = convertProperty(resolved, root);
    }
  }

  const urlPath = path.replace(/\{(\w+)\}/g, '${$1}');

  const methodMap = {
    get: 'request.get',
    post: 'request.post',
    put: 'request.put',
    delete: 'request.delete',
    patch: 'request.patch'
  };

  const requestFn = methodMap[method] || 'request.get';
  const args = [`\`${urlPath}\``];
  if (bodySchema) args.push('data');
  if (queryParams.length > 0) args.push('params');

  return `export function ${funcName}(${params.join(', ')}): Promise<${returnType}> {\n  return ${requestFn}(${args.join(', ')});\n}\n`;
}

export async function generateTypesFromOpenAPI(openApiUrlOrPath, outputPath, options = {}) {
  let spec;

  if (openApiUrlOrPath.startsWith('http')) {
    const response = await fetch(openApiUrlOrPath);
    spec = await response.json();
  } else {
    const content = await fs.readFile(openApiUrlOrPath, 'utf-8');
    spec = JSON.parse(content);
  }

  const lines = [
    '// Auto-generated by openapi-contract-sync.js',
    '// DO NOT EDIT MANUALLY - regenerate with: npm run contract:sync',
    `// Generated at: ${new Date().toISOString()}`,
    `// Source: ${openApiUrlOrPath}`,
    '',
    "import { request } from './client';",
    '',
  ];

  if (spec.components?.schemas) {
    for (const [name, schema] of Object.entries(spec.components.schemas)) {
      lines.push(generateInterface(toPascalCase(name), schema, spec));
      lines.push('');
    }
  }

  if (spec.paths && options.includeApi !== false) {
    lines.push('// API Functions');
    lines.push('');

    for (const [path, methods] of Object.entries(spec.paths)) {
      for (const [method, operation] of Object.entries(methods)) {
        if (['get', 'post', 'put', 'delete', 'patch'].includes(method)) {
          lines.push(generateApiFunction(method, path, operation, spec));
          lines.push('');
        }
      }
    }
  }

  const output = lines.join('\n');
  await fs.ensureDir(path.dirname(outputPath));
  await fs.writeFile(outputPath, output, 'utf-8');

  const interfaceCount = Object.keys(spec.components?.schemas || {}).length;
  const endpointCount = Object.values(spec.paths || {}).reduce(
    (sum, methods) => sum + Object.keys(methods).filter(m => ['get', 'post', 'put', 'delete', 'patch'].includes(m)).length, 0
  );

  return {
    outputPath,
    interfaceCount,
    endpointCount,
    success: true
  };
}

export async function validateContractConsistency(openApiUrlOrPath, frontendApiDir, options = {}) {
  let spec;
  if (openApiUrlOrPath.startsWith('http')) {
    const response = await fetch(openApiUrlOrPath);
    spec = await response.json();
  } else {
    const content = await fs.readFile(openApiUrlOrPath, 'utf-8');
    spec = JSON.parse(content);
  }

  const inconsistencies = [];
  const backendEndpoints = new Set();

  for (const [path, methods] of Object.entries(spec.paths || {})) {
    for (const method of Object.keys(methods)) {
      if (['get', 'post', 'put', 'delete', 'patch'].includes(method)) {
        backendEndpoints.add(`${method.toUpperCase()} ${path}`);
      }
    }
  }

  if (options.checkFrontend && frontendApiDir) {
    const frontendFiles = await fs.glob('**/*.ts', { cwd: frontendApiDir });
    for (const file of frontendFiles) {
      const content = await fs.readFile(path.join(frontendApiDir, file), 'utf-8');
      const urlMatches = content.matchAll(/['"`](\/api\/v1\/[^'"`]+)['"`]/g);
      for (const match of urlMatches) {
        const frontendUrl = match[1];
        const normalizedUrl = frontendUrl.replace(/\$\{[^}]+\}/g, '{id}');
        const found = [...backendEndpoints].some(ep => {
          const epPath = ep.split(' ').slice(1).join(' ');
          return epPath === normalizedUrl || epPath === normalizedUrl.replace(/\/$/, '');
        });
        if (!found) {
          inconsistencies.push({
            type: 'frontend_no_backend',
            file,
            url: frontendUrl,
            message: `前端调用 ${frontendUrl} 在后端OpenAPI中未找到对应端点`
          });
        }
      }
    }
  }

  if (options.checkSchemaTypes && spec.components?.schemas) {
    for (const [name, schema] of Object.entries(spec.components.schemas)) {
      const resolved = resolveRef(schema, spec);
      if (resolved.properties) {
        for (const [fieldName, fieldSchema] of Object.entries(resolved.properties)) {
          const resolvedField = resolveRef(fieldSchema, spec);
          if (resolvedField.type === 'string' && (fieldName.endsWith('_at') || fieldName.endsWith('_time') || fieldName === 'created_at' || fieldName === 'updated_at')) {
            if (!resolvedField.format || resolvedField.format !== 'date-time') {
              inconsistencies.push({
                type: 'datetime_type_mismatch',
                schema: name,
                field: fieldName,
                message: `${name}.${fieldName} 可能是datetime字段但类型为string，建议使用format: date-time`
              });
            }
          }
        }
      }
    }
  }

  return {
    consistent: inconsistencies.length === 0,
    backendEndpointCount: backendEndpoints.size,
    inconsistencies,
    summary: inconsistencies.length === 0
      ? '前后端契约一致'
      : `发现 ${inconsistencies.length} 个不一致`
  };
}
